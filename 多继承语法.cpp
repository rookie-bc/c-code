#include <iostream>
using namespace std;

//多继承语法          //实际开发不建议使用
class Base1
{
public:
	Base1()
	{
		m_A = 100;
	}

	int m_A;
};
class Base2
{
public:
	Base2()
	{
		m_A = 200;
	}

	int m_A;
};
//多继承  需要继承Base1和Base2           //用逗号分割
//语法  : class 子类 : 继承方式 父类1 ， 继承方式 父类2 .....
class Son : public Base1, public Base2            
{
public:
	Son()
	{
		m_C = 300;
		m_D = 400;
	}
	int m_C;
	int m_D;
};

void test1()
{
	Son s;
	cout << "size of s:" << sizeof(s) << endl;

	//cout << "m_A = " << s.m_A << endl;//如果父类所有属性不同名，则不用加作用域

	//当父类中出现同名成员，需要加作用域区分
	cout << "Base1 m_A = " << s.Base1::m_A << endl;
	cout << "Base2 m_A = " << s.Base2::m_A << endl;
}
int main()
{
	test1();
	return 0;
}